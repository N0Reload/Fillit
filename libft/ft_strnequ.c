#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	if (n <= 0)
		return (1);
	if (*s1 == *s2 && !(*s1))
		return (1);
	if (*s1 != *s2)
		return (0);
	return (ft_strnequ(s1 + 1, s2 + 1, n - 1));
}
